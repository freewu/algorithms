# In this topic, let's do a simple exercise of the add command. 
# Please use the git command to complete the operation of adding changes to the staging area, and go through the following 5 steps:

# Step 1 Enter the code repository directory of my-repo and check the current modification status
cd my-repo
git status

# Step 2 Use the git add command to add file-1.txt in the directory of my-repo to the temporary storage area
git add file-1.txt


# Step 3 View the current modification status again
git status

# Step 4 Use the git add . command to add all files to the temporary storage area
git add . 

# Step 5 View current status area
git status